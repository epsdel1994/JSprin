j_ex.symetry = function(x,sym){
  if((A1 <= x) && (x <= H8)){
    if((sym & 1) !== 0){
      x = ((x & 0x00000038) | (7 - (x & 7)));
    }
    if((sym & 2) !== 0){
      x = ((0x00000038 - (x & 0x00000038)) | (x & 7));
    }
    if((sym & 4) !== 0){
      x = (((x & 0x00000038) >>> 3) | ((x & 7) << 3));
    }
  }
  return x;
};
